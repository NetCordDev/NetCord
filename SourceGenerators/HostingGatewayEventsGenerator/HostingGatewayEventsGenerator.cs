        }

        stringWriter.WriteIndentation(1);
        stringWriter.WriteLine("}");
    }

    private static IEventSymbol[] GetEvents(ImmutableArray<INamedTypeSymbol> typeSymbols)
    {
        return [.. typeSymbols.SelectMany(symbol => symbol.GetMembers()).OfType<IEventSymbol>()];
    }
}
